public class ProgramaPrincipal {

    public static void main(String args[]){
        // Sentencia if-else
        var numero = -10;
        // Verificamos si es un valor positivo
        if (numero > 0){
            System.out.println("Valor positivo: " + numero);
        }
        else{
            System.out.println("Valor Cero o Negativo: " + numero);
        }
    }
}
