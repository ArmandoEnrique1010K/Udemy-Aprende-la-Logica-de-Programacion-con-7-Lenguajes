# Arreglos en Python
# Declarar un arreglo (lista)
numerosArreglo = []
# Modificar los valores de un arreglo
numerosArreglo.append(13)
numerosArreglo.append(14)
numerosArreglo.append('Hola')
# Accedemos a los valores del arreglo
print(f'Elemento 1 arreglo[0] = {numerosArreglo[0]}')
print(f'Elemento 2 arreglo[1] = {numerosArreglo[1]}')
print(f'Elemento 3 arreglo[2] = {numerosArreglo[2]}')
