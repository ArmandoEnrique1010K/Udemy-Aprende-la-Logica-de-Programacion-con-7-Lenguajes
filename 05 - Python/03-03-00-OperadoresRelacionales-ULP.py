# Operadores relacionales (comparacion)
a = 5
b = 6

# Operador igualdad ==
print(f'{a} == {b} -> {a == b}')

# Operador distinto !=
print(f'{a} != {b} -> {a != b}')

# Operador Menor que
print(f'{a} < {b} -> {a < b}')

# Operador Menor o igual que
print(f'{a} <= {b} -> {a <= b}')

# Operador Mayor que
print(f'{a} > {b} -> {a > b}')

# Operador Mayor o igual que
print(f'{a} >= {b} -> {a >= b}')
