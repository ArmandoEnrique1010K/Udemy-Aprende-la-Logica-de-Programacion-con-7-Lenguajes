# Funciones Incorporadas en Python

# Subcadena en Python
cadena = 'Hola Mundo'
# No incluye el indice final (indice final - 1)
subcadena1 = cadena[0:10]
print(subcadena1)
# Subcadena de Hola
subcadena2 = cadena[0:4]
print(subcadena2)
# Subcadena de Mundo
subcadena3 = cadena[5:]
print(subcadena3)
subcadena4 = cadena[5:10]
print(subcadena4)
subcadena5 = cadena[:]
print(subcadena5)