# Arreglos en Python
# Declarar un arreglo (list)
numeros_arreglo = [13, 14, 'Hola']
# Modificar los valores del arreglo
numeros_arreglo[0] = 15
numeros_arreglo[1] = 22
numeros_arreglo[2] = 'Adios'
#numeros_arreglo[3] = 90
# Accedemos a los valores del arreglo (list)
print(f'Arreglo[0] = {numeros_arreglo[0]}')
print(f'Arreglo[1] = {numeros_arreglo[1]}')
print(f'Arreglo[2] = {numeros_arreglo[2]}')

